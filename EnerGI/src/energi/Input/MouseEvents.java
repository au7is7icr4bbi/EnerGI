package energi.Input;

/**
 * Represents the different mouse events.
 * @author andrew
 * @version 1.0
 */
public enum MouseEvents
{
	/**
	 * Delta X
	 */
	DeltaX,
	/**
	 * Delta Y
	 */
	DeltaY,
	/**
	 * Delta wheel
	 */
	DeltaWheel,
	/**
	 * Absolute X
	 */
	AbsX,
	/**
	 * Absolute Y
	 */
	AbsY,
	/**
	 * Left Mouse Button
	 */
	ButtonLeft,
	/**
	 * Right Mouse Button
	 */
	ButtonRight,
	/**
	 * Middle Mouse Button
	 */
	ButtonMiddle
}
