package energi.Input;

/**
 * Represents the buttons of a standard gamepad
 * @author andrew
 * @version 1.1
 */
public enum Buttons
{
	/**
	 * A/X Button (B on SNES)
	 */
	A,
	/**
	 * B/Circle Button (A on SNES)
	 */
	B,
	/**
	 * X/Square Button (Y on SNES)
	 */
	X,
	/**
	 * Y/Triangle button (X on SNES)
	 */
	Y,
	/**
	 * L button (not trigger!)
	 */
	L,
	/**
	 * R Button (not trigger!)
	 */
	R,
	/**
	 * Mode/Xbox button. Not commonly used, except perhaps for an achievements overlay
	 */
	Mode,
	/**
	 * Start button
	 */
	Start,
	/**
	 * Select/back button
	 */
	Select;
}
